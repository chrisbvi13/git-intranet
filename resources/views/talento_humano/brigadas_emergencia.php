            <p class="comunicado__paragraph comunicado__subtitle">¿Qué es una emergencia?</p>
            <p class="comunicado__paragraph--content">Es un evento indeseable, identificable en el tiempo, que produce un estado de perturbación funcional en el sistema, capaz de afectar las actividades cotidianas de una comunidad, alterando los servicios e impidiendo el normal desarrollo de las actividades esenciales. Una emergencia puede generar víctimas y daños materiales que afectan la estructura social y económica, lo cual exige una respuesta inmediata con los recursos normalmente disponibles.</p>
            <p class="comunicado__paragraph comunicado__subtitle">¿Qué es la Brigada de emergencias?</p>
            <p class="comunicado__paragraph--content">Es un grupo de trabajadores que se encuentran debidamente organizados, entrenados y equipados para identificar y reaccionar ante emergencias, actuando, controlando o minimizando oportunamente las consecuencias de dichos riesgos identificados.</p>
            <p class="comunicado__paragraph comunicado__subtitle">Objeto de la brigada de emergencias</p>
            <p class="comunicado__paragraph--content">Las brigadas son formadas con el fin de promover un cambio de actitud, orientando a los colaboradores a una cultura preventiva que se fundamente y respalde en la necesidad de controlar y manejar de manera organizada las condiciones laborales inadecuadas que pueden causar desastres y atenten contra la salud o seguridad de los trabajadores o los bienes materiales de la empresa.</p>
