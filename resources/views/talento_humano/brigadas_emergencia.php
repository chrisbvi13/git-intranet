              <h5>FUNCIONES GENERALES DE LA BRIGADA DE EMERGENCIA</h5>
              <p class="comunicado__paragraph comunicado__subtitle">ANTES</p>
              <p class="comunicado__paragraph--content vineta m-left">Conocer ampliamente las instalaciones, sus procesos y los riesgos que puedan generar emergencias.</p>
              <p class="comunicado__paragraph--content vineta m-left">Identificar claramente la ubicación y capacidad de los equipos para el control de emergencia.</p>
              <p class="comunicado__paragraph--content vineta m-left">Realizar inspección de seguridad que beneficien el desarrollo de las funciones laborales.</p>
              <p class="comunicado__paragraph--content vineta m-left">Avisar sobre cambios en el comportamiento de los riesgos en las instalaciones.</p>
              <p class="comunicado__paragraph--content vineta m-left">Presentar informes sobre recursos necesarios para el control de emergencias.</p>
              <p class="comunicado__paragraph--content vineta m-left">Reconocer los procedimientos operativos instaurados para el control de emergencias.</p>
              <p class="comunicado__paragraph comunicado__subtitle">DURANTE</p>
              <p class="comunicado__paragraph--content vineta m-left">Ejecutar los procedimientos operativos normalizados</p>
