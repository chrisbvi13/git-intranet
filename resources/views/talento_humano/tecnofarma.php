            <p class="comunicado__paragraph--content">La <span class="comunicado__subtitle font-w">tecnovigilancia</span> está definida como el conjunto de actividades que tienen por objeto la identificación y la cualificación de los efectos adversos serios e indeseados producidos por equipos y demás dispositivos médicos, así como la identificación de los factores de riesgo asociados a estos efectos o características, con base en la notificación, registro y evaluación sistemática de los problemas relacionados con los dispositivos médicos, para determinar la frecuencia, gravedad e incidencia de los mismos y prevenir su aparición.</p>
            <p class="comunicado__paragraph--content">La <span class="comunicado__subtitle font-w">farmacovigilancia</span> se encarga de la detección, la evaluación y la prevención de los riesgos asociados a los medicamentos una vez comercializados, nos ayuda a la detección temprana de las reacciones adversas, a la identificación de factores de riesgo, la estimación de los aspectos cuantitativos de la relación beneficio/riesgo y la difusión de la información necesaria para mejorar la regulación y prescripción de medicamentos.</p>
            <p class="comunicado__paragraph--content">Nos permite hacer uso racional y seguro de los medicamentos, la evaluación y comunicación de los riesgos y beneficios de los medicamentos comercializados, la educación y la información a los pacientes</p>
